def main():
    number_1 = float(input("Digite o primeiro número: "))
    number_2 = float(input("Digite o segundo número: "))
    numbers_added_together = number_1 + number_2

    print(f"A soma do número {number_1} com o {number_2} é : {numbers_added_together}")


if __name__ == "__main__":
    main()
